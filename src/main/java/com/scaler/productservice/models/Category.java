package com.scaler.productservice.models;

import jakarta.persistence.Entity;

@Entity
public class Category extends BaseModel {
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
